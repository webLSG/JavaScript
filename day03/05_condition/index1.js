/** switch */
const season = "spring";

switch (season) {
  case "spring":
    console.log("봄");
    break;
  case "summer":
    console.log("여름");
    break;
  case "fall":
    console.log("가을");
    break;
  case "winter":
    console.log("겨울");
    break;
  default:
    console.log("알수없음");
    break;
}
